#!/bin/sh

B='\e[90m'
Z='\e[0m'
F='\e[34m'
Y='\e[31m'
R='\e[95m'

printf "${B}::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::${Z}\n"
printf "${B}:::'${F}##${B}::::'${F}##${B}:'${F}##${B}::::'${F}##${B}::'${F}######${B}:::'${F}######${B}:::'${Y}#######${B}::'${F}########${B}::'${F}########${B}::::${Z}\n"
printf "${B}::: ${F}##${B}:::: ${F}##${B}: ${F}##${B}:::: ${F}##${B}:'${F}##${B}... ${F}##${B}:'${F}##${B}... ${F}##${B}:'${Y}##${B}${R}#####${B}${Y}##${B}: ${F}##${B}.... ${F}##${B}: ${F}##${B}.....:::::${Z}\n"
printf "${B}::: ${F}##${B}:::: ${F}##${B}: ${F}##${B}:::: ${F}##${B}: ${F}##${B}:::..:: ${F}##${B}:::..:: ${Y}##${B}${R}#####${B}${Y}##${B}: ${F}##${B}:::: ${F}##${B}: ${F}##${B}::::::::::${Z}\n"
printf "${B}::: ${F}#########${B}: ${F}##${B}:::: ${F}##${B}:. ${F}######${B}:: ${F}##${B}::::::: ${Y}##${B}${R}#####${B}${Y}##${B}: ${F}########${B}:: ${F}######${B}::::::${Z}\n"
printf "${B}::: ${F}##${B}.... ${F}##${B}:. ${F}##${B}:: ${F}##${B}:::..... ${F}##${B}: ${F}##${B}::::::: ${Y}##${B}${R}#####${B}${Y}##${B}: ${F}##${B}.. ${F}##${B}::: ${F}##${B}...:::::::${Z}\n"
printf "${B}::: ${F}##${B}:::: ${F}##${B}::. ${F}##${B} ${F}##${B}:::'${F}##${B}::: ${F}##${B}: ${F}##${B}::: ${F}##${B}: ${Y}##${B}${R}#####${B}${Y}##${B}: ${F}##${B}::. ${F}##${B}:: ${F}##${B}::::::::::${Z}\n"
printf "${B}::: ${F}##${B}:::: ${F}##${B}:::. ${F}###${B}::::. ${F}######${B}::. ${F}######${B}::. ${Y}#######${B}:: ${F}##${B}:::. ${F}##${B}: ${F}########${B}::::${Z}\n"
printf "${B}:::..:::::..:::::...::::::......::::......::::.......:::..:::::..::........:::::${Z}\n"
printf "${B}::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::${Z}\n"

[ -r /etc/lsb-release ] && . /etc/lsb-release

if [ -z "$DISTRIB_DESCRIPTION" ] && [ -x /usr/bin/lsb_release ]; then
	# Fall back to using the very slow lsb_release utility
	DISTRIB_DESCRIPTION=$(lsb_release -s -d)
fi

printf "\n"
printf "\e[0;42;30m    \e[4;42;30m%s\e[0;42;30m (%s %s %s)                     \e[0m\n" "$DISTRIB_DESCRIPTION" "$(uname -o)" "$(uname -r)" "$(uname -m)"
printf "\n"
